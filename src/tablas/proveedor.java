package tablas;

public class proveedor {

	public int id_prov;
	public String nom_prov;
	public String dir_prov;
	public String tel_prov;
	public String correo_prov;
	
	public proveedor(int id_prov, String nom_prov, String dir_prov, String tel_prov, String correo_prov) {
		
		this.id_prov = id_prov;
		this.nom_prov = nom_prov;
		this.dir_prov = dir_prov;
		this.tel_prov = tel_prov;
		this.correo_prov = correo_prov;
		
	}

}

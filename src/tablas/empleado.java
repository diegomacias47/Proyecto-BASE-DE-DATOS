package tablas;

public class empleado {

	public int id_emp;
	public String nom_emp;
	public String dir_emp;
	public String edad_emp;
	public String tel_emp;
	public int id_tra;
	
	public empleado(int id_emp, String nom_emp, String dir_emp, String edad_emp, String tel_emp, int id_tra) {
		
		this.id_emp = id_emp;
		this.nom_emp = nom_emp;
		this.dir_emp = dir_emp;
		this.edad_emp = edad_emp;
		this.tel_emp = tel_emp;
		this.id_tra = id_tra;
		
	}
	
}
